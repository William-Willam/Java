package Exercicios.Condicionais;

public class VerficacaoParImpar {
    public static void main(String[] args) {
        
        int numero = 0;

        if (numero % 2 == 0) {
            System.out.println(numero + " é par !");
        }else{
            System.out.println(numero + " é impar !");
        }
    }
}
