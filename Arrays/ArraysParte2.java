package Arrays;

public class ArraysParte2 {
    public static void main(String[] args) {
        
        // declaração
        String[] nomes;

        // popular o array
        nomes = new String[]{"William", "Gabriel", "Jubileu", "Chico" , "Cebolinha"};
        
        // For - each
        for (String i : nomes) {
            System.out.println(i);
        }
    }
}
