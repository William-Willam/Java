
public class Operadores {
    public static void main(String[] args) {
        // variaveis
        int a = 10;
        int b = 2;

        //processamento e saida
        System.out.println(a + b);
        System.out.println();
        System.out.println(a - b);
        System.out.println();
        System.out.println(a * b);
        System.out.println();
        System.out.println(a / b);
        System.out.println();
        System.out.println(a % b);
    }    
}
